fn main() {
    println!("Hello, world!");
    println!("My name is {} and I'm {} years old", "Kevin", 29);
    println!("a + b = {}", 4+2);
    println!("{0} has a {2} and {0} has a {1}", "Kevin", "cat", "dog");
    println!("{name} {surname}", surname="Brown", name="Kevin");
    println!("Binary : {:b}, Hex : {:x}, Octal : {:o}", 15, 15, 15);
    println!("Array : {:?}", [1, 2, 3]);
}
